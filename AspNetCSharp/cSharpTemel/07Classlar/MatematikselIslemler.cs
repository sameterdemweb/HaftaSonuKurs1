using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace _07Classlar
{
    public class MatematikselIslemler
    {
        public int Toplama(int sayi1, int sayi2)
        {
            return sayi1 + sayi2;
        }

        public int Cikartma(int sayi1, int sayi2)
        {
            return sayi1 - sayi2;
        }
        public int Bolme(int sayi1, int sayi2)
        {
            return sayi1 / sayi2;
        }
        public int Carma(int sayi1, int sayi2)
        {
            return sayi1 * sayi2;
        }
    }
}
